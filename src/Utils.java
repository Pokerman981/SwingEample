public class Utils {

    public static void messageConsole(int message) {
        System.out.println(message);
    }

    public static void messageConsole(String message) {
        System.out.println(message);
    }

    public static void messageConsole(double message) {
        System.out.println(message);
    }

    public static void messageConsole(boolean message) {
        System.out.println(message);
    }

}
